#include "./../../includes/miniRT.h"

t_matrix_4	matrix_4_shearing(t_vector v)
{
	t_matrix_4	m;

	m = matrix_4_new();
	m.m[0][0] = 1;
	m.m[0][1] = v.x;
	m.m[0][2] = v.y;
	m.m[1][0] = v.z;
	m.m[1][1] = 1;
	m.m[1][2] = v.z;
	m.m[2][0] = v.y;
	m.m[2][1] = v.x;
	m.m[2][2] = 1;
	m.m[3][3] = 1;
	return (m);
}
